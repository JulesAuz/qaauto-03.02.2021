        // 8 bits from -128 to 127
        byte bt = 127;

        // 16 bits from -32768 to 32767
        short shrt =32767;

        // 32 bits from -2,147,483,648 to 2,147,483,647
        int integer = 2147483647;

        // 64 bits from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
